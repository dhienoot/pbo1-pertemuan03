package Percabangan;

public class Switch {
    public static void main(String[] args) {
        int JalanJalan=3;
        switch(JalanJalan){
            case 1:
                System.out.println("orang gila");
                break;
            case 2:
                System.out.println("orang kesambet");
                break;
            case 3:
                System.out.println("vave sagan ubat nyamuk");
                break;
            case 4:
                System.out.println("urang lamak");
                break;
            default:
                System.out.println("urang halus");
                break;

        }
    }
}
