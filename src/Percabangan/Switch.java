public class Switch {
    public static void main(String[] args) {
        char nilai='D';
        switch(nilai){
            case 'A':
                System.out.println("Mhs    : \"Terima Kasih Pak\"");
                System.out.println("Dosen  : \"Congrats yahh !!\"");
                break;
            case 'B':
                System.out.println("Mhs    : \"Kenapa nilai ulun rendah Pak?\"");
                System.out.println("Dosen  :\"Kam Copas tarus pang ketahuanku\"");
                break;
            case 'C':
                System.out.println("Mhs    :\"datang mana pian tahu pa\"");
                System.out.println("Dosen  :\"ada ja informasi dari kawanan kam\"");
                System.out.println("Mhs    :\"Dustainya haja tuh..hihi\"");
                break;
            default:
                System.out.println("Mhs    :\"datang mana pian tahu pa?ulun kadada copas pa\"");
                System.out.println("Dosen  :\"bujur jua kah\"");
                System.out.println("Dosen  :\"hadang tumat aku nelponi kawan kam\"");
                System.out.println("Dosen  :\"Sedang telponan dengan teman sekelas mahasiswa tersebut\"");
                System.out.println("Mhs    :\"bukah mencincing\"");
                break;

        }
    }
}
